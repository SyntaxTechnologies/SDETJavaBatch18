package org.class2;

public class E4Concat {
    public static void main(String[] args) {

        String fName="Kawoon";
        String lName="Imtiaz";
        String space=" ";
        System.out.println(fName+space+lName);
        System.out.println(fName+" "+lName);
        System.out.println("Kawoon"+" "+"Imtiaz");
    }
}
