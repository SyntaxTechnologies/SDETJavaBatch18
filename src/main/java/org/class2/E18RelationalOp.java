package org.class2;

public class E18RelationalOp {
    public static void main(String[] args) {

        int num1=10;
        int num2=20;

        System.out.println(num1>num2); //false
        System.out.println(num1<num2); //true
        //num1 is equal(num1==num2) to num2
        System.out.println(num1==num2); //false
        // num1 is not equal(num1!=num2) to num 2
        System.out.println(num1!=num2); //true
    }
}
