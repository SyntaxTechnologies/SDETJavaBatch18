package org.class3;
/*
Write a Java Program to check whether number is Even or Odd.
 */
public class T6 {
    public static void main(String[] args) {

        int number=13;

        if(number%2==0){
            System.out.println(number+" is an even number");
        }else{
            System.out.println(number+" is odd");
        }
    }
}
