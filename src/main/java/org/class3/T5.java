package org.class3;

/*
Write a program to check whether number is positive or negative.
 */
public class T5 {
    public static void main(String[] args) {

        int number=-10;
        if(number>0){
            System.out.println("Positive");
        }else if(number<0){
            System.out.println("Negative");
        }else{
            System.out.println("Zero");
        }

    }
}
